            <h2 class="title-section">Capitalismo y consumismo</h2>
            <p>El club de la pelea es una reflexión crítica acerca de la sociedad de consumo en la cual vivimos y los efectos que ella provoca en los individuos. La película comienza mostrando varias marcas famosas y el modo en que las personas consumen esos productos con el objetivo de llenar el vacío interior.

